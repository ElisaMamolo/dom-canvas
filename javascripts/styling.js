
/*COLORS

fillStyle = color; <== Sets the style used when filling shapes.
strokeStyle = color; <== Sets the style for shapes’ outlines.
NOTE
When you set the strokeStyle and/or fillStyle property, the new value becomes 
the default for all shapes being drawn from then on.

For every shape you want in a different color,
 you will need to reassign the fillStyle or strokeStyle property.
*/

/* TRANSPARENCY
globalAlpha = transparencyValue

Applies the specified transparency value to all 
future shapes drawn on the canvas. The value must
 be between 0.0 (fully transparent) to 1.0 (fully opaque).
 */

 /*LINE STYLES
lineWidth = value Sets the width of lines drawn in the future. Values must be positive numbers. By default this value is set to 1.0 units.

lineCap = type Sets the appearance of the ends of lines. There are three possible values for this property and those are: butt, round and square. 
By default this property is set to butt.

lineJoin = type Sets the appearance of the 
“corners” where lines meet. 
There are three possible values for this property:
 round, bevel and miter. By default this property is set to miter.
 */


